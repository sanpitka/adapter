public interface ShapeBasic {

    /**Draws the Shape by Printing the Shape on screen
     * 
     */
    public void draw();
    /**Prints Growing the Shape
     * 
     */
    public void grow();
    /**
     * 
     * @return The Shape name as A string
     */
    public String printText();
    /**
     * 
     * @return if shape is in Visible return true, else return false
     */
    public boolean isVisible();
}
